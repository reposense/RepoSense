package reposense.dataobject;

/**
 * Represents a github issue's index number
 */
public class GithubIssueIndex {

    private int index;

    /**
     * Constructs a {@code GithubIssueIndex}.
     *
     * @param index A valid description.
     */
    public GithubIssueIndex(int index) {
        this.index = index;
    }
}
